int main (void)
{
	char myStr[50] = "";
	char rev[50] = "";
	int idx = 0, index = 0;

	printf("Enter the string\n");

	gets(myStr);

	for (idx = strlen(myStr) - 1; idx >= 0; idx--)
	{
		rev[index++] = myStr[idx];
	}

	rev[index] = '\0';

	printf("Reverse string = %s\n", rev);

	return 0;
}








