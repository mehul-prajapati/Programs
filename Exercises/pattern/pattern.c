int main (void)
{
	unsigned char startChar = 'a';	
	unsigned int count = 0, idx, charCnt;
	
	printf("Enter count\n");
	scanf("%d", &count);

	for (idx = 0; idx < count; idx++)
	{
		for (charCnt = 0; charCnt <= idx; charCnt++)
		{
			printf("%c", startChar);
			startChar += 1;
		}

		printf("\n");
		startChar = 'a';
	}

	return 0;
}








