//==================================================================
//================= @INCLUDES ======================================
//==================================================================
#include <stdio.h>
#include "MsgQue.h"
#include <unistd.h>

//==================================================================
//================= @DEFINES =======================================
//==================================================================
#define	MODULE_NAME			"RECV_DATA"
#define	MODULE_ID			1

//==================================================================
//================= @ENUMS =========================================
//==================================================================

//==================================================================
//================= @DATATYPES =====================================
//==================================================================
static MSG_QUE msgQue;

//==================================================================
//================= @FUNCTION PROTOTYPE ============================
//==================================================================

//==================================================================
//================= @FUNCTION DEFINITION ===========================
//==================================================================
int main (void)
{
	char buffer[MSG_BUF_SIZE_MAX] = "";
	char recvName[NAME_SIZE_MAX] = "";

	MSG_QUE_Init(&msgQue, MSG_QUE_KEY_1, MODULE_ID, MODULE_NAME);

	while (TRUE)
	{	
		if (MSG_QUE_Recv_Data(&msgQue, 0, recvName, buffer))
		{
			printf("Receiving from %s: %s\n", recvName, buffer);
		}
	}
	
	return 0;
}


